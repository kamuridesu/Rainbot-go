func HasObsceneWord(text string) error {

	for word := range strings.SplitSeq(strings.ToLower(text), " ") {
		if slices.Contains(Wordlist, word) {
			slog.Warn(fmt.Sprintf("HOW: User usou palavra proibida: %s", word))
			return fmt.Errorf("Ei! Você falou uma palavra proibida, cadê sua educação?")
		}
	}
	return nil

}
